package Practica01;

public class Ecuaciones212 {
  public static void main(String[] args) {
    int xx = 2;
    a = 1;
    System.out.println(a * xx * xx * xx + 7 + " "+ "Correcta");
    System.out.println(a * xx * xx * (xx + 7));
    System.out.println((a * xx) * xx * (xx + 7));
    System.out.println((a * xx) * xx * xx + 7 + " "+ "Correcta");
    System.out.println(a * (xx * xx * xx) + 7 + " "+ "Correcta");
    System.out.println(a * xx * (xx * xx + 7));
    // a, d, e ya no le dan prioridad a la suma
  }
}
